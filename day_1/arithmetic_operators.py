a = 10
b = 3

# REPL--> repeat evaluate print loop

# addition
print(a + b)

# subtraction
print(a - b)

# multiplication
print(a * b)

# division (--> float)
print(a / b)

# integral division (a // b = floor(a / b)) -> int
print(a // b)

# power operator (a ^ b)
print(a ** b)

# Modulus Operator / Remainder operator (a % b = a - (a // b) * b)
print(a % b)
